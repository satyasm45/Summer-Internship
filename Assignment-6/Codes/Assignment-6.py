##Generating all lines
x_AB = line_dir_pt(m,P,k1,k2)
x_BC = line_dir_pt(m,Q,k1,k2)
#Plotting all lines
plt.plot(x_AB[0,:],x_AB[1,:],label='$Tangent1$')
plt.plot(x_BC[0,:],x_BC[1,:],label='$Tangent2$')


